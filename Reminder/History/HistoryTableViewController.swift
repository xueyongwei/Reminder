    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        self.refreshHistory()
    }

    func refreshHistory(){
        self.dataSource.removeAll()
        if let history = Persistent.store.queryAllHistory(){
            self.dataSource = history
        }
        self.tableView.reloadData()
    }
    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        
        return self.dataSource.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "HistoryTableViewCell", for: indexPath) as! HistoryTableViewCell
        let history = self.dataSource[indexPath.row]
        cell.nameLabel.text = history.name
        cell.markeDate.text = history.createDate?.reminderFormater()
        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        let history = self.dataSource[indexPath.row]
        let descVC = self.storyboard?.instantiateViewController(withIdentifier: "HistoryDescViewController") as! HistoryDescViewController
        descVC.history = history
        self.navigationController?.pushViewController(descVC, animated: true)
    }

